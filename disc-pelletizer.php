            <p>The Mars Mineral Disc Pelletizer consists of a pan, rocker deck, plow support with plows, spray system and ust cover which are supported on a heavy structural frame and base.</p>
			
            <p>The pan is driven by heavy-duty gear reducer that is mounted directly on the output shaft.</p>
			
            <div class="caption">
            	<img src="images/Disc-Pelletizer-back.jpg" alt="Disc Pelletizer back" />
                <span>Mars Mineral's Disc Pelletizer is equipped with binder-spray system for use in micro-pelletizing, pelletizing or conditioning of a wide range of materials.</span>
            </div>
			
            <p>Changes in pan rotational speed can be made through an optional control panel with a VFD. The pan angle is adjustable within a range of 20 degrees to suit various pelletizing</p>

            <p>The Mars Mineral Disc Pelletizer is equipped with a binder spray system which includes piping, tubing, 
                nozzles, pressure gauges, valves, pressure regulator and strainer. For extremely dusty or toxic 
                applications, Mars Mineral will also provide a discharge chute and a dust cover as optional items.</p>

            <p>This unit is a front feeding device. Various feeders are used
                in conjunction with it to accommodate a range of materials and accuracies. Selection may be made from volumetric
                screw feeders, belt or table feeders and gravimetric weigh belt feeders.</p>
                
                
                

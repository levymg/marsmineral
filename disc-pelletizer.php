                applications in which a shallow pan and a smaller retained
                burden are desired. It can be used to pelletize materials such as 
                taconites, copper, nickel, lead and zinc concentrates. Other applications 
                include pelletizing of reclaimed sludge, moist filter cakes, coal, flue dusts, limestone, fertilizers and ores.</p>
            

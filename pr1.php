<h3>Mars Mineral Wins Two Contracts to Provide Deep Drum Pelletizers</h3>
<p>Mars, PA, November 8, 1999 -- Mars Mineral, a manufacturer of pelletizing equipment and systems, has won contracts from F.L. Smidth & Co. and Fuller International, Inc. to supply Deep Drum Pelletizers to process cement kiln bypass dust into pellets. </p>
<p>"These contracts are for the third and fourth Deep Drum Pelletizers supplied to the Egyptian Cement industry. The agreements prove our ability to design, manufacture and install state-of-the-art equipment and systems that help companies meet environmental regulations," says Bob Hinkle, president of Mars Mineral. </p>
<p>The Deep Drum Pelletizer, designed and patented by Mars Mineral, includes a deep- drum unit with a rear-mounted auger feeder. Its deep-drum design makes it ideal for applications requiring long retention time for reaction, densification and production of large-diameter pellets. </p>
<p>Other pelletizers that Mars Mineral manufactures include the Disc, Pin-Mixer, Ampel Conditioner and Agglo-Miser. The Pin-Mixer and Ampel Conditioner can be used as mixers as well as pelletizers. Capacity of Mars Mineral pelletizers ranges from bench scale to 125 tons per hour.</p>
<p> Mars Mineral is a leader in the design and manufacture of agitation pelletizing equipment and systems. In addition to producing machinery that converts fine dusts and powders into pellets, the company offers materials testing, equipment rental, pelletizing systems and solutions. Mars Mineral is based in Mars, PA, about 20 miles north of Pittsburgh, and is a division of Woodward, Inc. </p>
    

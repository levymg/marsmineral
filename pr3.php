	<h3>Mars Mineral Upgrades Pelletizing Solutions for Improved Handling of Tire Pyrolysis Char</h3><br />

    <p>Mars, PA, October 7, 2010 -- Mars Mineral, a leader in the design and production of pelletizing systems, has expanded its agglomeration solutions by providing post-processing pelletizing for companies that perform tire pyrolysis. After processing used tires under extreme heat, recyclers take the resulting char, often referred to as &ldquo;carbon black,&rdquo; and pelletize it for use in pigment and reinforcement applications across a wide range of product categories. Applications include automotive accessories; coatings; asphalt shingles and other roofing materials; concrete and mortar; industrial and furniture products; pipe; rubber hoses, gaskets and belts; plastics; printing inks and toners; film; and consumer electronics.</p>
    <p>&ldquo;Pelletizing adds value to the complex process of recovering tire components by densifying the char and minimizing dust to improve the material's handling characteristics,&rdquo; says Clayton Woodward, president of Mars Mineral. &ldquo;As companies look for new revenue streams, pelletization of tire pyrolysis char offers numerous market opportunities.&rdquo;</p>
    <p>Mars Mineral is a leader in the design and manufacture of agitation pelletizing systems. In addition to producing machinery that converts fine dusts and powders into spherical-shaped pellets, the company also offers materials testing, equipment rental, systems design, and engineering &amp; installation. The company, based in Mars, PA, about 20 miles north of Pittsburgh, is a division of Woodward Inc. For more information, contact <strong>Bob Hinkle</strong>, Mars Mineral, at <strong>724.538.3000</strong>, or e-mail <a href="mailto:bhinkle@marsmineral.com">bhinkle@marsmineral.com</a>.</p>
    

            <p>The Mars Mineral Pin Mixer features a cylindrical, stationary shell with a length-to-diameter ratio between 4:1 and 5:1. Inside the shell is a replaceable rubber liner and a shaft with radially-extending rows of pins in a helix pattern. There is a close tolerance between the tips of the pins and the inside
                of the mixer shell.</p>
			
            <p>Shaft rotational speed, and therefore tip speed, is high
                (several hundred RPM), which is higher than the speed of traditional paddle mixers, pug mills and Ampel conditioners.</p>
			
            <p>The material enters at one end of the cylinder and is whipped by the pins as it moves from the inlet, through the shell and to the bottom outlet. As a fine spray of liquid is added at the entry section and distributed throughout the powder, fine mixing and micro-agglomeration will occur. The end result is
                a wetted, agglomerated and densified micro-pellet.</p>


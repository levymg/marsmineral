	<h3>Craig Vaughan Named General Manager of Mars Mineral</h3>

        <p>
            Craig Vaughan, a senior executive with an extensive technical background, has been hired as general manager of Mars Mineral, a leader in the design 
            and production of agitation pelletizing equipment and systems. Mr. Vaughan is responsible for 
            the company’s business operations, and for the development of new products and markets. Prior 
            to joining Mars Mineral, Mr. Vaughan held a variety of engineering management positions with 
            Spang & Co. and Calgon Corporation, where he became the inventor of 20 U.S., European and 
            Canadian patents for additives used in paper processing, water treatment, and skin and hair care. 
            Mr. Vaughan also headed up the Materials Division of Penn State University’s Electro-Optics Center in Freeport, PA. He 
            holds an MBA from Carnegie Mellon University, an M.S. in Chemistry from Duquesne 
            University and a B.S. in Biochemistry from Princeton University.</p>

        <p>
            “Craig’s breadth and depth of experience, and record of achievement in strategic 
            planning, operations management, product development and marketing, should help us diversify 
            our customer base and take advantage of new market opportunities,” says Clayton Woodward, 
            president of Woodward Inc., Mars Mineral’s parent company. “His superior business and technical skills are ideally suited to our growth plans.”
        </p>
        
        <p>
            Mars Mineral manufactures agitation pelletizing equipment and systems. The company 
            also offers a wide range of research and testing services, and pilot scale development. 
            Mars Mineral’s 45,000 square-foot production facility is located in Mars, PA, near Pittsburgh. 
            The company is a division of Woodward Inc., which specializes in the design, fabrication and 
            assembly of ferrous and non-ferrous metal products used in bulk materials processing and storage applications, including metals, cement, and construction equipment manufacturing; 
            mining & aggregates; food processing; and power generation. Woodward Inc. also offers a 
            variety of design and engineering services. Woodward Inc. has been in business for more than 

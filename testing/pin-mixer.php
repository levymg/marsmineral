            <h1>Pin Mixer</h1>
			
            <p>The Mars Mineral Pin Mixer is a pin-type solids processor designed for applications requiring high energy input to materials for mixing or micro-pelletizing. It is a high speed, conditioning and micro-pelletizing device that converts dust into small agglomerates through the action of a high speed rotor shaft and pin assembly and the addition of liquids such as water, binders, oil or surfactants.</p>
			
            <p>The Pin Mixer is ideally suited for applications requiring extreme micro-pelletizing, mixing-conditioning with small pellet size control and preparation of a feed mixture for further processing in a pelletizer. Materials agglomerated in the Pin Mixer include: carbon black, cement kiln dust, pigments, coal dust, pesticides, electric furnace baghouse dust, limestone fines, graphite, coke, petroleum coke fines, bauxite and silica fume.</p>
            
            
			

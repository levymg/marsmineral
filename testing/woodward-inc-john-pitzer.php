            <h1>Woodward Inc. Names John Pitzer General Manager</h1>
            
            <p><strong>Mars, PA, February 19, 2013</strong> &mdash; Woodward Inc., a leading designer and fabricator of ferrous and non-ferrous metal products in bulk material handling applications, has hired John Pitzer as general manager. In that capacity, Mr. Pitzer is responsible for Woodward's daily business operations, and those of the company's Mars Mineral division which designs and manufactures agitation pelletizing equipment and systems. Prior to assuming his current position, Mr. Pitzer held a variety of engineering management positions in manufacturing and retail environments. He has more than 30 years of experience related to coil processing, machine tooling, packaging, material handling and sortation.</p>
    
    <p>"John's experience thriving in lean manufacturing environments and dealing with the requirements of a diverse customer base should help us capitalize on new market opportunities," says Clayton Woodward, president of Woodward Inc. "His combination of technical and managerial skills is precisely what we need to strengthen our competitiveness."</p>
    
    <p>Woodward Inc. specializes in the design, fabrication and assembly of ferrous and non-ferrous metal products used in a variety of processing and storage applications, including metals, cement, and construction equipment manufacturing; mining &amp; aggregates; food processing; and power generation. The company also offers a full range of design and engineering services. Woodward's Mars Mineral division manufactures agitation pelletizing equipment and systems. The company's 45,000 square-foot production facility is located in Mars, PA, near Pittsburgh. Woodward Inc. has been in business for more than 50 years.</p>
    

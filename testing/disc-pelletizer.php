            <p>The Mars Mineral Disc Pelletizer consists of a pan, drive and plows, which are supported on a heavy structural frame and base. </p>
			
            <p>The pan is driven by an 1800 rpm TEFC motor through a vari-drive belt and heavy-duty gear reducer. It is mounted on the output shaft of the reducer for disc sizes from three feet to eight feet in diameter. </p>
			
            <div class="caption">
            	<img src="images/Disc-Pelletizer-back.jpg" alt="Disc Pelletizer back" />
                <span>Mars Mineral's Disc Pelletizer is equipped with binder-spray system for use in micro-pelletizing, pelletizing or conditioning of a wide range of materials.</span>
            </div>
			
            <p>Disc pelletizers 10 feet in diameter and larger are equipped with constant speed cog-belt drives and are driven through motor, reducer, pinion and ring gear mounted behind the pan. Changes in pan rotational speed can be made through the proper selection of sheaves and belts. </p>

            <p>The pan angle is adjustable within a range of 20 degrees to suit various pelletizing conditions and applications. </p>
            
            <p>Pan speeds of disc pelletizers three feet to eight feet in diameter are variable by a 3:1 ratio. </p>
            
            <p>The Mars Mineral Disc Pelletizer is equipped with a binder spray system which includes piping, tubing, nozzles, pressure gauges, valves, pressure regulator and strainer. For extremely dusty or toxic applications, Mars Mineral will also provide a discharge chute and a dust cover as optional items. </p>
            
            <p>This unit is a front feeding device. Various feeders are used in conjunction with it to accommodate a range of materials and accuracies. Selection may be made from volumetric screw feeders, belt or table feeders and gravimetric weighbelt feeders. </p>
			

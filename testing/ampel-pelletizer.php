            <h1>Ampel Horizontal Pelletizer</h1>
			
            <p>The Mars Mineral Ampel Horizontal Pelletizer is a rapid-agitation device used to mix, condition and pelletize dust, fine powders and other materials. This equipment produces either a conditioned, wetted product or a granular, free-flowing agglomerate. It features a wide range of applications for conditioning and pelletizing at capacities ranging from one-half tph to 50 tph.</p>
			
            <p>The patented design of the Ampel pelletizer features a rubber bottom trough with a single-shaft rotor assembly that extends the length of the trough. Elongated mixing arms with specially-designed wedge-shaped mixing blades extend radially from the assembly. Either a variable or constant-speed drive is used to power the assembly.</p>
			
            <p>The blades are furnished in a variety of metal compositions. The sharp leading edges of the blades face the direction of rotor rotation, with the speed of rotor and blades adjusted to the correct movement and conditioning/pelletizing action and through-put of the material being processed.</p>


            <p>The bottom of the trough is formed of flexible rubber, which is attached to the stationary upper trough, producing a flexing surface that resists material buildup, improves material flow and increases wear resistance. For applications in which rubber cannot be used, other materials including metal or plastic bottoms may be used. </p>
			
            <p>The Ampel is equipped with a water spray system above the inlet section of the pelletizer to apply finely atomized water spray or binder additives. </p>
			
            <p>The Ampel unit provides granulation of agricultural chemicals, fertilizers, pesticides, chemical consumer products, carbon powders and flue dust from dust collectors. It conditions flyash, cement or lime kiln dust, foundry dust, iron oxide, friction material waste, lead fume and zinc oxide. </p>
			
            <p>The Ampel is also ideal for processing moist difficult-to-feed materials, combining moist and dry feed, mixing two or more materials and preparing feed materials for subsequent processing. </p>


            <p>The Deep Drum Pelletizer’s low headroom requirement allows for installation in existing structures and the patented, rear-feed auger provides an excellent opportunity to mix dry binders
            when required.</p>

            <p>The drum of this pelletizer provides 30° angle adjustment and incorporates a binder spray system and adjustable wear-resistant scrapers. Other features of the unit include 1800 rpm motors
            with gear reducers and the specially-designed auger feeder.</p>

            <p>Material enters the drum center through the feeder from the rear. This feeder consists of a larger conditioning auger which surrounds the feed auger. The conditioning auger arrangement eliminates material bridging in the feed hopper.</p>
            
            <p>Because the feed enters below already-processed material in
                the drum, dusting is kept to a minimum. This feature augments the unit’s compact design, making it particularly suited for
                installation in space-restricted areas.</p>


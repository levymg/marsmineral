            <h3>The design of our customized pelletizing systems is a five-step process</h3>
            
            <h4>Step 1</h4>
            <p>In our laboratory, we determine the pelletizing characteristics of your material and establish equipment requirements for your particular needs.</p>
            <h4>Step 2</h4>
            <p>Using laboratory information, Mars Mineral's engineering staff develops equipment layouts and process instrumentation diagrams for your system.</p>
            <h4>Step 3</h4>
            <p>We select the proper equipment and fabricate it in our own shop and contract with vendors to build your system components to specification.</p>
            <h4>Step 4</h4>
            <p>Mars Mineral delivers the finished equipment to your site, installs foundations and sets the equipment into place.</p>
            <h4>Step 5</h4>
            <p>We provide our system start-up expertise and train your operators.</p>

